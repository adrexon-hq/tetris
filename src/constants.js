export function getKickData(type, from, to) {
  if (type === "O") return [[0, 0]];
  const key = `${from}>${to}`;
  return (type === "I" ? I_KICKS[key] : JLSTZ_KICKS[key]) ?? [[0, 0]];
}
export function getKickData180(type, from, to) {
  if (type === "O") return [[0, 0]];
  const key = `${from}>${to}`;
  return KICKS_180[key] ?? [[0, 0]];
}


